def get_description(resource_id: int) -> str:
    if 0x07d0 <= resource_id <= 0x0bb6:
        return "Path Information (saved paths)."
    elif 0x0fa0 <= resource_id <= 0x1387:
        return "Plug-In resource(s)"
    else:
        return descriptions.get(resource_id, "")
